package com.java;

import java.util.Iterator;

public class LoopsConcept {

	public static void main(String[] args) {

		// 1. While loop
		// disadvantage of while loop: it will generate infinite loop if we dont give incremental or dremental par
		// print 1 to 10
		int i = 1; // initialization
		while (i<=10) {// conditional 
			System.out.println(i);
			i++; // incremental or decremental
		}

		System.out.println("******************");

		// 2.for loop

		//print 1 to 10
		for (int j = 0; j <=10; j++) {  //Initialization, conditional, incremental
			System.out.println(j);
		}

		//print 10 to 1
		for (int j = 10; j <=1; j--) {
			System.out.println(j);
			
			
			
			
			

		}



	}

}
